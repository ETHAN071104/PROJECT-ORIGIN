function translateCore(source: string): string {
  const normalized = source
    .replaceAll('\u00a0', ' ')
    .replaceAll('â€œ', '“').replaceAll('â€', '”').replaceAll('â€™', '’')
    .replaceAll('Ã—', '×').replaceAll('â€”', '—').replaceAll('â†’', '→').replaceAll('â†', '←')
  const exact = ZH[source] ?? ZH[normalized]
  if (exact) return exact
  for (const [pattern, render] of PATTERNS) {
    const match = source.match(pattern)
    if (match) return render(...match.slice(1))
  }
  return source
}

export function translateText(language: Language, source: string): string {
  if (language === 'en' || !source.trim()) return source
  const leading = source.match(/^\s*/)?.[0] ?? ''
  const trailing = source.match(/\s*$/)?.[0] ?? ''
  return `${leading}${translateCore(source.trim())}${trailing}`
}

export function isSupportedLanguage(value: unknown): value is Language {
  return value === 'en' || value === 'zh-CN'
}
